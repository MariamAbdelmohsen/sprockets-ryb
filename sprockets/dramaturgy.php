            <h3>
                <a id="defense"></a>
                Defense Contractors &amp; Private Military Industrial Complex
            </h3>
        <p>
            As drones rose in popularity, so did defense contractors &#40;Blackwater is the most famous, but there are hundreds of companies who pay for private police forces, military-style escorts, food and water supplies, and weapons development&#41;.
        </p>
        <p>
            The official end of the Cold War in 1989 saw the flourishing of the private military industry&mdash;rather than fighting with nation-based military forces, companies began fighting each other, for pseudo-nationalistic reasons.
        </p>
        <p>
            Defense contractors have long developed weapons and military technology for the United States and other countries &#40;Boeing, Lockheed-Martin, Halliburton, and dozens of other US-based manufacturers have received large defense contracts since WWII&#41;. What changed since the 1990’s was the number of companies specializing in military-style defense development, and the size of the contracts from various countries. Between 1995 and 2005, the Pentagon signed over 3,000 contracts with Private Military Forces &#40;PMFs&#41;. These groups have operated in over 50 nations, and on every continent except for Antarctica.
        </p>
        <p>
            Casualties among PMFs are not recognized, as people employed by these companies are contract employees, and not military personnel. They are not honored with military honors, they and their families do not receive veterans benefits, and their deaths are rarely reported on the news. While the United States military has deescalated its involvement in the Middle East, especially Iraq and Afghanistan, PMFs often take their place&mdash;training local police and military forces, hunting company-specified targets, and performing operations obscured even from the Pentagon itself. Like too many of their victims, PMFs on the ground are civilians, and are treated as such.
        </p>
        <p>
            Legally, they are also not mercenaries &#40;which are illegal according to United Nations’ treaties&#41; but the line defining these two is blurry. Because they are only accountable for completing their contracts, PMFs do not report their methods, technologies, or expenses very often. They can operate outside of national treaties if they are considered multinational corporations, although this, too, is a legal gray area being questioned, and which will likely lead to numerous lawsuits in the next several decades.
        </p>
