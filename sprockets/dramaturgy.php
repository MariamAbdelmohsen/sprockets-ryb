            <h3>
                    <a id="vr"></a>Virtual Reality
            </h3>
        <p>
            In general, <em>virtual reality</em> refers to an artificial experience of a different world, curated by an individual &#40;including a machine like a computer&#41;, and intended to completely immerse the viewer using multiple forms of sensory input. While we typically think of VR in terms of headsets and video games right now, the concept of virtual reality goes back at least to theatre revolutionary Antonin Artaud, who described the immersive nature of this art form in his essay collection, The Theatre and Its Double &#40;in fact, the 1958 English translation of this work uses the first instance of the term &ldquo;virtual reality&rdquo;&#41;.
        </p>
        <p>
            *Note: Artaud is also the originator of &ldquo;theatre of cruelty&rdquo;
        </p>
        <p>
            Morton Heilig also wrote of an &ldquo;Experience Theatre&rdquo; in which the audience’s senses would be encompassed by the production. In 1962, he built his Sensorama, a prototype for this encompassing that is essentially television with added sensory experiences: smell and touch, on top of sight and sound.
        </p>
        <p>
            Since 1959, &ldquo;virtual&rdquo; has applied to computer simulations, in the sense of the program creating an immersive world that does not exist in reality, but is made to appear real.
        </p>
