                </h3>
        <p>
            The phrase &ldquo;Achievement Unlocked&rdquo; comes from Xbox and PlayStation gaming systems, which incorporated mini-goals in the games into &ldquo;achievements.&rdquo; These are collected by the player across several games, and can be displayed in their profiles like trophies.
