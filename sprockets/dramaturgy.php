            <img src="images/ryb-dt/acbadge1.png" alt="AC Badge" title="Achievement Unlocked" class="ac">
            <img src="images/ryb-dt/acbadge2.png" alt="AC Badges" title="Achievement Unlocked" class="ac">
        <p>
            The idea dates back to 1982, when Activision introduced in-game &ldquo;patches&rdquo; for various high scores. Game manuals instructed players to achieve these goals as extra bonuses, beyond just beating bosses or levels. E-Motion from Amiga, in 1990, was the first to introduce other specific achievements into the game, although they were called &ldquo;secret bonuses.&rdquo; The term &ldquo;achievement&rdquo; was introduced in 2005 as part of Microsoft Xbox’s Gamerscore system, leading to the specific Xbox 360 badge, above.
        </p>
        <p>
            The modern idea of &ldquo;Achievement Unlocked&rdquo; has become a cultural meme, with variants including &ldquo;Left the House,&rdquo; &ldquo;Put on Pants,&rdquo; or similar.&mdash;<a href="https://en.wikipedia.org/wiki/Achievement_(video_gaming)" target="_blank">Wikipedia</a>
