                <h3>
                    <a id="dubai"></a>Dubai, The United Arab Emirates &#40;UAE&#41;
                </h3>
        <p>
            Located on the southeastern coast of the Persian Gulf, Dubai is an oil-rich megacity that has been very built-up in the last 10 to 15 years. It is promoted as a resort or vacation destination, and features thousands of manmade structures like islands or indoor ski slopes to attract rich travelers and investors from everywhere.
        </p>
        <p>
            Dubai is in the capital emirate of the seven emirates which make up the UAE, called the Emirate of Dubai. This emirate, and Abu Dhabi, are the only two UAE emirates which have veto power over matters of national importance. Dubai is also scheduled to host the World Expo 2020.
        </p>
        <p>
            The city is the most expensive city in the Middle East, and the 22<sup>nd</sup> most expensive city in the world. It is also rated &#40;by Western-based global consulting firms&#41; as the best place in the Middle East to live.
        </p>
        <p>
            Although the city has been investing in fascinating architectural tricks, skyscrapers, and infrastructure projects since it focused its oil wealth in the 1960’s and 70’s, the city and surrounding areas have been inhabited by humans since early human history. The earliest written mention of the city of Dubai, specifically, is found in the <em>Book of Geography</em> written in 1095 by Andalusian-Arab geographer, Abu Abdullah al-Bakri.&mdash;<a href="https://en.wikipedia.org/wiki/Dubai" target="_blank">Wikipedia</a>
