            <h4>
                Virtual Reality War
            </h4>
        <p>
            Virtual reality is used to design combat situations or a &ldquo;virtual war&rdquo; for use as a training tool. A simulated war zone is an ideal way to train soldiers for engagement with the enemy but in a controlled way. It removes the risks associated with a real world combat situation such as death or injury. <em><a href="https://www.vrs.org.uk/virtual-reality-military/war.html" target="_blank">From The Virtual Reality Society, UK</a></em>
        </p>
        <p>
            VR Combat Situation
        </p>
        <p>
            The idea behind this is to prepare soldiers for a real, live combat situation. It teaches ways of dealing with unexpected events, for example, a sniper attack, but without putting themselves in danger. It is vitally important that they are taught how to react to dangerous settings where the wrong decision may mean the difference between life and death.
        </p>
        <p>
            This is particularly important when training soldiers to become platoon leaders or in any other position of responsibility. These soldiers know that they are in charge of a group of fit and eager men and women who have to be trained in front line combat. Plus there are the many complex dynamics associated with leading a platoon which can only be learnt in a training situation.
        </p>
        <p>
            So where does virtual reality fit into all of this? It teaches soldiers about the importance of communication and following orders. It also instructs them in combat techniques and means of survival. Plus it shows them ways of navigating and dealing with what is often a hostile environment. Here they learn how to deal with hostile locals as well as being on guard for the unexpected such as IED’s &#40;improvised unexploded bombs&#41;, suicide bombers or sniper attacks.
        </p>
        <p>
            A simulation of a war zone enables inexperienced soldiers to learn and handle high stress situations. They can be taught how to take out a target or how to respond to enemy fire.
        </p>
        <p>
            Virtual reality is not just used for on the ground scenarios: it is also used to train combat pilots, for example helicopter pilots who have to navigate an aircraft in difficult conditions, e.g. night time flying. They use a flight simulator and wear a head mounted display &#40;HMD&#41; which enables them to experience a change in perception as they move their head.
        </p>
